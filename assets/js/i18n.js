// Função para obter idioma do navegador
function getSystemLanguage() {
  const lang = navigator.language || navigator.userLanguage;
  return lang.startsWith('fr') ? 'fr' : 'pt';
}

// Função para definir idioma
function setLang(lang) {
  if (!translations[lang]) lang = 'pt';
  localStorage.setItem('lang', lang);
  applyTranslations(lang);
}

// Função para aplicar traduções
function applyTranslations(lang) {
  const trans = translations[lang];
  
  document.querySelectorAll('[id]').forEach(el => {
    const key = el.id;
    if (trans[key]) {
      if (el.tagName === 'TEXTAREA' || (el.tagName === 'INPUT' && el.type !== 'checkbox')) {
        el.placeholder = trans[key];
      } else {
        el.innerHTML = trans[key];
      }
    }
  });
  
  // Atualizar atributo lang
  document.documentElement.lang = lang === 'fr' ? 'fr' : 'pt-BR';
  
  // Atualizar botão de idioma
  updateLangButton(lang);
}

// Função para atualizar botão de idioma
function updateLangButton(lang) {
  const btn = document.querySelector('.lang-toggle');
  if (btn) {
    btn.textContent = lang === 'pt' ? 'FR' : 'PT';
    btn.onclick = () => setLang(lang === 'pt' ? 'fr' : 'pt');
  }
}

// Inicializar idioma na página
document.addEventListener('DOMContentLoaded', () => {
  let lang = localStorage.getItem('lang');
  if (!lang) {
    lang = getSystemLanguage();
    localStorage.setItem('lang', lang);
  }
  applyTranslations(lang);
});

// Permitir mudar idioma via URL
const urlParams = new URLSearchParams(window.location.search);
if (urlParams.get('lang')) {
  setLang(urlParams.get('lang'));
}